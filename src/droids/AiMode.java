package droids;

/**
 * Перерахування (enum) режимів AI, яке обиратиме користувач у меню.
 * "Логіка", яку задає користувач.
 */
public enum AiMode {
    AGGRESSIVE, // Завжди атакувати найслабшого
    DEFENSIVE,  // Атакувати, але захищатися/тікати, якщо здоров'я < 30%
    SUPPORT     // (Для Хілера) Спершу лікувати, потім атакувати
}